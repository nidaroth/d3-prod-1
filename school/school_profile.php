            console.log("Elementos SBPAY no encontrados - continuando sin validación");
            return true;
        }
        
        // Validar cada campo
        if ($merchantId.val().trim() === '') {
            errors.push('Merchant ID es requerido');
            $merchantId.addClass('error').closest('.form-group').addClass('has-error');
            isValid = false;
