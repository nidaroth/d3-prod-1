            errors.push('Secret Key es requerido');
            $secretKey.addClass('error').closest('.form-group').addClass('has-error');
            isValid = false;
        }
        
        if (!isValid) {
            alert('Errores en la configuración de CyberSource:\n\n' + errors.join('\n'));
        }
        
        return isValid;
